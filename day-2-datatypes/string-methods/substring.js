// substring(): It takes two arguments, the starting index and the stoppping index but it doesn't include the stopping index.
let string = 'JavaScript'
console.log(string.substring(0,4)) // Java
console.log(string.substring(4,10)) // Script
console.log(string.substring(4)) // Java
let country = 'Indonesia'
console.log(country.substring(0)) // Indonesia
console.log(country.substring(0,4)) // Indo
console.log(country.substring(4)) // nesia

